import UIKit
import UptakeToolbox



public extension UIView {
  /// The opposite of `isHidden`.
  public var isVisible: Bool {
    get {
      return ❗️isHidden
    }
    set {
      isHidden = ❗️newValue
    }
  }
}



public extension UIControl {
  /// The opposite of `isEnabled`.
  public var isDisabled: Bool {
    get {
      return ❗️isEnabled
    }
    set {
      isEnabled = ❗️newValue
    }
  }
}
